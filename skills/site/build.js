#!/usr/bin/env node
/**
 * Generate skills/site/data.js from skills/catalog.json + skills/site/glossary.json.
 *
 * The static skills explorer (index.html / glossary.html) is fully client-side
 * and reads window.SKILLS and window.GLOSSARY from the generated data.js — no
 * fetch, no server, no dependencies. This mirrors how the source curriculum
 * site generated its data.js from a catalog.
 *
 * Usage:
 *   node skills/site/build.js            # write skills/site/data.js
 *   node skills/site/build.js --check    # exit 1 if data.js is stale
 */
'use strict';

const fs = require('fs');
const path = require('path');

const SITE_DIR = __dirname;
const SKILLS_DIR = path.resolve(SITE_DIR, '..');
const CATALOG_PATH = path.join(SKILLS_DIR, 'catalog.json');
const GLOSSARY_PATH = path.join(SITE_DIR, 'glossary.json');
const OUT_PATH = path.join(SITE_DIR, 'data.js');
const REPO_TREE = 'https://github.com/iii-hq/iii/tree/main';

function readJson(p) {
  return JSON.parse(fs.readFileSync(p, 'utf8'));
}

function buildSkills(catalog) {
  const skills = Array.isArray(catalog.skills) ? catalog.skills : [];
  return skills.map((s) => ({
    name: s.name,
    title: s.title || s.name,
    description: s.description || '',
    path: s.path || '',
    url: s.doc ? `${REPO_TREE}/${s.doc}` : '',
    bytes: s.bytes || 0,
  }));
}

function buildGlossary(raw) {
  return (Array.isArray(raw) ? raw : []).map((g) => ({
    term: g.term || '',
    means: g.means || '',
    says: g.says || '',
  }));
}

function render(skills, glossary) {
  return (
    '/* AUTO-GENERATED by skills/site/build.js — do not edit by hand. */\n' +
    'window.SKILLS = ' +
    JSON.stringify(skills, null, 2) +
    ';\n\n' +
    'window.GLOSSARY = ' +
    JSON.stringify(glossary, null, 2) +
    ';\n'
  );
}

function main() {
  const check = process.argv.includes('--check');

  if (!fs.existsSync(CATALOG_PATH)) {
    console.error(
      `skills/catalog.json not found — run ` +
        `python3 .github/scripts/build_skills_catalog.py first.`
    );
    process.exit(1);
  }

  const skills = buildSkills(readJson(CATALOG_PATH));
  const glossary = buildGlossary(readJson(GLOSSARY_PATH));
  const payload = render(skills, glossary);

  if (check) {
    const existing = fs.existsSync(OUT_PATH) ? fs.readFileSync(OUT_PATH, 'utf8') : '';
    if (existing !== payload) {
      console.error('skills/site/data.js is out of date — run `node skills/site/build.js`');
      process.exit(1);
    }
    console.log(`data.js is up to date (${skills.length} skills, ${glossary.length} terms)`);
    return;
  }

  fs.writeFileSync(OUT_PATH, payload, 'utf8');
  console.log(`wrote ${path.relative(process.cwd(), OUT_PATH)} ` +
    `(${skills.length} skills, ${glossary.length} terms)`);
}

main();
